void main(){

  var a=10;
  var b=5;

  print("Arithmatic operators:");
  print(a+b);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);

  print("Unary operators:");
  var x=20;
  print(x++);
  print(x++);

  var y=25;
  print(++y);
  print(++y);

  var z=30;
  print(z--);
  print(z--);

  var s=35;
  print(--s);
  print(--s);






}